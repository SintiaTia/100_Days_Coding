package Days;

public class TipeDataCharDay42 {
    public static void main(String[] args) {
        char startKarakter = 'S';
        char endKarakter = 'Z';
        
        for (char i = startKarakter; i <= endKarakter; i++) {
            System.out.print(i+" ");
        }
        System.out.println(" ");
    }
}
