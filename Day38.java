package Days;



public class Day38 {
    public static void main(String[] args) {
        byte angkaPertama = 10;
        byte angkaKedua = 7;
        
        byte penjumlahan =  (byte) (angkaPertama + angkaKedua);
        System.out.println("Penjumlahan: " + penjumlahan);
        
        byte pengurangan = (byte) (angkaPertama - angkaKedua);
        System.out.println("Pengurangan: " + pengurangan);
        
        byte perkalian = (byte) (angkaPertama * angkaKedua);
        System.out.println("Perkalian: " + perkalian);
        
        byte pembagian = (byte) (angkaPertama / angkaKedua);
        System.out.println("Pembagian: " + pembagian);
    }
}
