package Days;

public class BooleanDay95 {
    public static void main(String[] args) {
        int angka = 7;
        int bilangan = 3;
        
        boolean I = (angka == bilangan);
        boolean L = (angka > bilangan);
        boolean Y = (angka < bilangan);
        
        System.out.println("apakah angka sama dengan bilangan? " + I);
        System.out.println("apakah angka lebih besar dari bilangan? " + L);
        System.out.println("apakah angka lebih kecil dari bilangan? " + Y);
    }
}
