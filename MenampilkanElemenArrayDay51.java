package Days;

public class MenampilkanElemenArrayDay51 {
    public static void main(String[] args) {
         //Program Menampilkan Elemen Array
         int sintia [] = {9,7,2,4,8,3};
         
         System.out.print("Elemen-elemen dalam array: ");
         for (int i = 0; i < sintia.length; i++) {
             System.out.print(sintia[i] + ",");
        }
         System.out.println("");
    }
}
